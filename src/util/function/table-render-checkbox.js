export default (h, change)=>{
    return h(
        "div",
        {
            class:{
                "woo-checkbox":true,
                "woo-checkbox-box-checked":false,
                "woo-table-checkbox":true,
            }
        },
        [
            h(
                "div",
                {
                    class:{
                        "woo-checkbox-box":true
                    }
                },
                [
                    h(
                        "div",
                        {
                            class:{
                                "woo-checkbox-box-color":true
                            }
                        },
                        [
                            h(
                                "span",
                                {
                                    class:{
                                        "material-icons":true,
                                        "woo-not-select":true,
                                        "woo-not-select-check":true
                                    }
                                },
                                [
                                    "check"
                                ]
                            ),
                            h(
                                "input",
                                {
                                    class:{
                                        "woo-checkbox-input":true
                                    },
                                    attrs:{
                                        type:"checkbox",
                                        checked:false
                                    },
                                    style:{
                                        position:"absolute",
                                        top:"0px",
                                        left:"0px",
                                        right:"0px",
                                        bottom:"0px",
                                        height: "16px",
                                        width: "16px",
                                        opacity:0,
                                        margin:0,
                                    },
                                    on:{
                                        change:(e)=>{
                                            if(e.target.checked){
                                                e.target.parentNode.parentNode.parentNode.classList.add("woo-checkbox-box-checked");
                                            }else{
                                                e.target.parentNode.parentNode.parentNode.classList.remove("woo-checkbox-box-checked");
                                            }
                                            typeof change === "function"?change(e.target.checked):null;
                                        }
                                    }
                                },
                                [
                                    "check"
                                ]
                            )
                        ]
                    )
                ]
            )
        ]
    );
};